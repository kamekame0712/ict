	</p>

	<h3>3.苦情及び相談</h3>
	<p>
		当社は、個人情報の取扱いに関する苦情及び相談を受けた場合、
		その内容について迅速に事実関係等を調査し、合理的な期間内に誠意をもって対応します。
	</p>

	<h3>4.権利の尊重</h3>
	<p>
		当社は、個人情報に関する個人の権利を尊重し、お客様からの個人情報の開示、訂正、利用停止等の請求および苦情・相談に対して遅滞なく対応します。
	</p>

	<h3>5.法令、国が定める指針その他の規範の遵守について</h3>
	<p>
		当社は、個人情報の取扱いに関する法令、国が定める指針その他の規範を遵守するとともに、
		日本工業規格「個人情報保護マネジメントシステム－要求事項」（JIS Q 15001）に準拠し、個人情報を保護します。
	</p>

	<h3>6.個人情報の継続的改善について</h3>
	<p>
		当社は、個人情報保護管理者を設置し、社会情勢・環境の変化等を踏まえて、継続的に“個人情報保護マネジメントシステム”を実施、維持していきます。
