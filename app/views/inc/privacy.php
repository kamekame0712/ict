	<h3>お客様が容易に認識できない方法による個人情報の取得について</h3>
	<p>
		当社サイトにおいては、お客様が容易に認識できない方法[クッキー（Cookies）]で個人情報を取得しております。
		クッキーはお客様が当社サイトに再度訪問された際、より便利に当サイトを閲覧していただくものであり、お客様のプライバシーを侵害するものではありません。
		またお客様のコンピューターへ悪影響を及ぼすこともありません。
		Google Chrome、Microsoft Edgeといったブラウザの設定により、クッキーの受取を拒否することも可能ですが、その場合でも当社サイトの閲覧に支障を来たすことはありません。
		ブラウザの設定方法は各ソフト製造元へお問合せ下さい。
	</p>
	<p>
		クッキーとはウェブサイトからお客様のコンピューターに送られる小容量の情報です。
		お客様が受け取り拒否しなければコンピューターのハードデスクに保存されます。
		クッキーはお客様のコンピューターを識別することができますが、お客様が個人情報を入力しない限りお客様自身を識別することは出来ません。
		当社サイトがハッキング等の被害にあったときに限り、被害解明のためクッキーを利用することがあります。
	</p>

	<h3>個人情報の安全管理措置について</h3>
	<p>
